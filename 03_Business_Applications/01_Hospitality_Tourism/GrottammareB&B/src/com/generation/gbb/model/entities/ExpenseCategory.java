package com.generation.gbb.model.entities;

/**
 * Enumeration of expense categories for financial classification.
 * Used to categorize expenses for reporting and budget management.
 * Each category represents a distinct type of business expenditure.
 */
public enum ExpenseCategory
{
    /** Food and beverage expenses (breakfast, restaurant supplies, etc.) */
    FOOD,
    
    /** Service-related expenses (utilities, maintenance, cleaning, etc.) */
    SERVICES,
    
    /** Employee salary and compensation expenses */
    SALARIES
}
